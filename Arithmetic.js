/*Arithmetic Operator
 +, -, *, /, % , ++ ,--
 */
 var a = 30;
 var b = 5;
 
 //addition
 var sum = a + b;
 console.log(sum);
 
 //Substraction
 var sub = a - b;
 console.log(sub);
 
 //Multiplication
 var mul = a * b;
 console.log(mul);
 
 //Division
 var div = a / b;
 console.log(div);
 
 //Modulus without reminder
 var modUnReminder = a % b;
 console.log(modUnReminder);
 
 //Modulus with reminder
 var b = 7
 var modReminder = a % b;
 console.log(modReminder);
 
 console.log();
 
 var c = 10;
 //Incremental Oparator
 
 //Pre Increment
 console.log(c)
 console.log(++c);
 console.log();
 var d = 15;
 //Post Increment
 console.log(d)
 console.log(d++);
 console.log(d);
 console.log();
 
 //Decremental Oparator
 
 var e = 40;
 //Pre Decrement
 console.log(e)
 var preDecrement = --e;
 console.log(e);
 
 //Post Decrement
 
 var f = 50;
 console.log(f)
 var preDecrement = f--;
 console.log(f);